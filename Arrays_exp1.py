cars = ["Ford", "Volvo", "BMW"]
# acessing first element in array;
car = cars[0]
print(car)
# using insert function
cars.insert(1,"maruti")
print(cars)
#using append()
cars.append("G-wagon")
print(cars)

# slicing methods
# printing whole array
print(cars[:])
# print from selected index to end
print(cars[2:])
# print selected range
print(cars[1:3])
# printing in reverse direction
print(cars[::-1])

